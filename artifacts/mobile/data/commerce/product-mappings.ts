const MAPPING_BY_ID = new Map<string, ProductMapping>(
  PRODUCT_MAPPINGS.map((m) => [m.internalId, m])
);

export function getProductMapping(internalId: string): ProductMapping | undefined {
  return MAPPING_BY_ID.get(internalId);
}

export function hasProductMapping(internalId: string): boolean {
  return MAPPING_BY_ID.has(internalId);
}

export function getMappedMaterials(): ProductMapping[] {
  return PRODUCT_MAPPINGS.filter((m) => m.internalType === 'material' && m.activeForCommerce);
}

export function getMappedTools(): ProductMapping[] {
  return PRODUCT_MAPPINGS.filter((m) => m.internalType === 'tool' && m.activeForCommerce);
}

export function getAllMappings(): readonly ProductMapping[] {
  return PRODUCT_MAPPINGS;
}
